public class Tugas2 {

    private double jari;
    private double diameter;
    private double luaspermukaan;
    private double volume;
    
    public void setjari(){
        this.jari = jari;
    }
    
    public double getjari(){
        jari = 14;
        return jari;
    }
    
    public double getDiameter(){
       diameter = jari + jari;
       return diameter;
    }
    
    public double getluasPermukaan(){
        luaspermukaan = jari * jari * Math.PI * 4;
        return luaspermukaan;
    }
    
    public double getvolume(){
        volume = jari * jari * jari * Math.PI * 4 / 3;
        return volume;
    }
    }
    

